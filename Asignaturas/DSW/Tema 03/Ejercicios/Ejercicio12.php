Ejercicio 12 - Programa en PHP que muestre un tablero de ajedrez, de 64 casillas, columnas de la a-
h y filas de la 1-8. Cuando el usuario haga "click" sobre una casilla, se le enviará información al
programa de las coordenadas de la casilla y el programa devolverá un nuevo tablero, coloreando las
casillas a las cuales podría saltar un alfil, desde la casilla indicada.