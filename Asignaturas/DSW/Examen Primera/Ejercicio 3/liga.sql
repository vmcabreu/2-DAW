DROP DATABASE IF EXISTS liga;
CREATE DATABASE liga DEFAULT CHARACTER SET utf8 COLLATE utf8_spanish_ci;
CREATE USER IF NOT EXISTS liga@localhost IDENTIFIED BY 'liga2022';
GRANT ALL ON liga.* TO 'liga'@'localhost';

use liga;

CREATE TABLE equipos (
    id int(6) auto_increment,
    nombre varchar(40) UNIQUE NOT NULL,
    PRIMARY KEY(id)
) ENGINE=InnoDB;

CREATE TABLE partidos (
    id int(6) auto_increment,
    local int(6),
    visitante int(6),
    resultado varchar(10) NOT NULL,
    PRIMARY KEY(id),
    FOREIGN KEY(local) REFERENCES equipos(id),
    FOREIGN KEY(visitante) REFERENCES equipos(id)
 ) ENGINE=InnoDB;

